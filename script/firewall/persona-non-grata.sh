#!/bin/bash
clear

source "/usr/local/turbolab.it/webstackup/script/base.sh"
printHeader "ğŸ›¡ï¸ persona-non-grata"
rootCheck

printMessage "Install UFW..."
apt install ufw -y

printMessage "Removing previous rules and disable the firewall..."
sudo ufw --force reset

printMessage "ğŸš¢ Allow connections from Docker containers..."
ufw allow from 172.17.0.0/16 to any

printMessage "ğŸ“ Allow FTP/FTPS (if installed)"
ufw allow 20,21,990,2121:2221/tcp

printMessage "ğŸ§ Allow SSH..."
ufw allow 22,222/tcp

printMessage "ğŸ’Œ Allow SMTP..."
ufw allow 25/tcp

printMessage "ğŸŒ Allow HTTP(s)..."
ufw allow 80,443/tcp

printMessage "â¬ Downloading IP block list..."
IP_BLACKLIST_FULLPATH=${WEBSTACKUP_AUTOGENERATED_DIR}persona-non-grata.txt
curl -Lo "${IP_BLACKLIST_FULLPATH}" https://raw.githubusercontent.com/TurboLabIt/webstackup/master/config/firewall/persona-non-grata.txt

printMessage "Insert a dummy rule (it's needed for scripting)"
ufw deny from 95.141.35.37 to any

while read -r line || [[ -n "$line" ]]; do
  FIRSTCHAR="${line:0:1}"
  if [ "$FIRSTCHAR" != "#" ] && [ "$FIRSTCHAR" != "" ]; then
    echo "Blocking: $line"
    ufw insert 1 deny from $line to any
  fi	
done < "$IP_BLACKLIST_FULLPATH"

printMessage "Remove the dummy rule"
ufw delete deny from 95.141.35.37 to any

printMessage "ğŸ”¥ğŸ”¥ Shields up! Activating the firewall..."
ufw --force enable 
